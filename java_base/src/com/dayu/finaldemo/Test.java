package com.dayu.finaldemo;

public class Test {
    // final 可以修饰类  变量  方法
    // 修饰类：成为最终类，特点时不能被继承了
    // 修饰方法：称为最终方法，特点时不能被重写
    // 修饰变量： 该变量只能被赋值一次

    // final修饰变量的注意
    // final修饰基本类型的变量，变量存储的数据不能被改变
    // final修饰引用类型的变量，变量存储的地址不能被改变，但地址所指向对象的内容时可以被改变的

    /**
     * 常量
     * 使用static final 修饰的成员变量就成为常量
     * Constant.NAME
     */
}
